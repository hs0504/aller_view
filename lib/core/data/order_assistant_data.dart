OrderAssistantCopy orderAssistantCopyFor(String languageCode) {
  return orderAssistantCopies[languageCode] ?? orderAssistantCopies['en']!;
}

String translatedAllergyName(String koreanName, String languageCode) {
  return allergyNameTranslations[koreanName]?[languageCode] ?? koreanName;
}

String buildOrderAssistantQuestion({
  required String languageCode,
  required String menuName,
  required List<String> allergyNames,
}) {
  final copy = orderAssistantCopyFor(languageCode);
  final allergies = allergyNames.join(', ');
  return copy.question
      .replaceAll('{allergies}', allergies)
      .replaceAll('{menuName}', menuName);
}
